export const translateTerm = (term, lang) => {
  if (!term) return '';
  const cleanTerm = term.trim();
  if (translations[lang] && translations[lang][cleanTerm]) {
    return translations[lang][cleanTerm];
  }
  return cleanTerm;
}

export const getTranslatedAdvisory = (bestMandi, state, crop, modalPrice, unit, lang) => {
  const transMandi = translateTerm(bestMandi, lang);
  const transState = translateTerm(state, lang);
  const transCrop = translateTerm(crop, lang);
  const transUnit = translateTerm(unit, lang);

  if (lang === 'te') {
    return `మేము సిఫార్సు చేస్తున్నాము: మీరు మీ ${transCrop} పంటను ${transState} లోని ${transMandi} మార్కెట్ (మండి) లో అమ్మవచ్చు, ఇక్కడ ధర ప్రస్తుతం గరిష్టంగా రూ. ${modalPrice} প্রতি ${transUnit} గా ఉంది. రాబోయే 4 రోజులలో ధరలు స్థిరంగా ఉండే అవకాశం ఉంది.`;
  }
  if (lang === 'hi') {
    return `हम आपको अपनी ${transCrop} फसल को ${transState} के ${transMandi} मंडी में बेचने की सलाह देते हैं, जहां वर्तमान में प्रति ${transUnit} मॉडल मूल्य रुपये ${modalPrice} के शिखर पर है। अगले 4 दिनों तक कीमतें स्थिर रहने की उम्मीद है।`;
  }
  return `Recommend selling your ${crop} at the ${bestMandi} Mandi (in ${state}) where the modal price is currently at a peak of Rs. ${modalPrice} per ${unit}. Prices are expected to remain steady for the next 4 days.`;
}
